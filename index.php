				<h1><a href="#5">Tamarind</a></h1>
				<h2>Named after the first thing I saw on my desk</h2>
				<time datetime="2019-09-07">Sep. 07, 2019</time>

				<p>

					The skin on my right foot big toe is dry and cracked, making it hard to even bend my toe without feeling any pain. But that aside, for a guy who always writes about not letting anxiety about the future rule him, I spent all of today thinking even more about the future. It's not long till I finish my Bachelor's Degree and begin looking for a full time job, and this seems like a huge step in my life. No more three-month long vacations, no mid-semester breaks, no relaxing. However, I am looking forward to being able to support myself and my family once I start earning a full time income. 	
					
				</p>
				
				<p>
				
					It seems that as time goes on, the monetary system works even less in favour of the humans who created it. House prices are skyrocketing, while the median wage rises slowly over the years; making it incredibly difficult for some people to have access to even the most basic housing. I am fearful that I will be trapped in this system just as I see many people trapped around me today, a slave to money. Somewhere in my mind, I wish I could live a simpler life, free of these worries, the way nature intended, but I realise that it is money that improved our standard of living exponentially. Where did we go wrong? Nonetheless, I will have to put up with this no matter what my opinion is, unless a better solution comes around.
				
				</p>
				
				<p>
				
					I have recently heard about things such as the 'two-day rule' which encourages making a commitment to doing some sort of exercise every second day at the least. I have been neglecting my health for a while now, not leaving my house and choosing to play video-games. I heard about this concept from <a href="https://www.youtube.com/user/blackboxfilmcompany">Matt D'Avella</a> who also produced some other videos which I really liked, <a href="https://www.youtube.com/watch?v=ATDYLTnGJV0">Simple Steps to Financial Freedom</a> being my favourite, which has motivated me to start thinking about my money more wisely. I have decided to try and implement these ideas in my life, let's see how this turns out.
				
				</p>

